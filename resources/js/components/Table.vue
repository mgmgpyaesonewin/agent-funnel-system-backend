<template>
  <div class="table-responsive">
    <div class="row">
      <div class="col-9">
        <h5 style="text-align: initial;line-height: 3rem;">
          Total
          <span class="badge badge-primary">{{ applicants.meta.total }}</span> records found.
        </h5>
      </div>
      <div v-show="webinarInvite === true" class="col-3">
        <button
          class="btn btn-info"
          data-toggle="modal"
          data-target="#webinarModal"
        >Invite to Webinar</button>
      </div>
    </div>
    <table class="table">
      <thead>
        <tr>
          <th v-show="webinarInvite === true"></th>
          <th>#</th>
          <th>Name</th>
          <th>Email</th>
          <th>Phone</th>
          <th>Age</th>
          <th>Status</th>
          <th>Actions</th>
        </tr>
      </thead>
      <tbody>
        <tr v-for="(applicant,i) in applicants.data" :key="i">
          <td v-show="webinarInvite === true">
            <fieldset>
              <div class="vs-checkbox-con vs-checkbox-primary">
                <input type="checkbox" v-model="webinarList" :value="applicant.id" />
                <span class="vs-checkbox">
                  <span class="vs-checkbox--check">
                    <i class="vs-icon feather icon-check"></i>
                  </span>
                </span>
              </div>
            </fieldset>
          </td>
          <th scope="row">{{ applicant.id}}</th>
          <td>{{ applicant.name}}</td>
          <td>{{ applicant.email}}</td>
          <td>{{ applicant.phone}}</td>
          <td>{{ applicant.age}}</td>
          <td>{{ applicant.status}}</td>
          <slot :applicant="applicant"></slot>
        </tr>
      </tbody>
    </table>
    <v-pagination :data="applicants" @pagination-change-page="getApplicants" align="center"></v-pagination>
    <div
      class="modal fade"
      id="webinarModal"
      tabindex="-1"
      role="dialog"
      aria-labelledby="exampleModalLabel"
      aria-hidden="true"
    >
      <div class="modal-dialog">
        <div class="modal-content">
          <div class="modal-header">
            <h5
              class="modal-title"
              id="exampleModalLabel"
            >Invite selected Applicants to attend Webinar</h5>
            <button type="button" class="close" data-dismiss="modal" aria-label="Close">
              <span aria-hidden="true">&times;</span>
            </button>
          </div>
          <div class="modal-body">
            <form>
              <div class="row">
                <div class="col">
                  <input type="text" class="form-control pickadate" placeholder="Date" />
                </div>
                <div class="col">
                  <input type="text" class="form-control" placeholder="Time" />
                </div>
              </div>
              <div class="row mt-1">
                <div class="col">
                  <input type="text" class="form-control" placeholder="Webinar URL" />
                </div>
              </div>
            </form>
          </div>
          <div class="modal-footer">
            <button type="button" class="btn btn-secondary" data-dismiss="modal">Close</button>
            <button type="button" class="btn btn-primary">Send Invites</button>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import Pagination from "laravel-vue-pagination";
import { EventBus } from "../event-bus.js";

export default {
  components: {
    "v-pagination": Pagination
  },
  props: ["status", "webinarInvite"],
  data() {
    return {
      applicants: [],
      webinarList: []
    };
  },
  methods: {
    updateApplicantsList(id) {
      console.log(`Applicant ${id}`);
      this.applicants.data = this.applicants.data.filter(
        applicant => applicant.id != id
      );
    },
    getApplicants(page = 1, status = 1, email = "", name = "") {
      status = this.status || status;
      window.API_URL = "http://127.0.0.1:8000/api";
      axios
        .get(
          `http://127.0.0.1:8000/api/applicants?status=${status}&email=${email}&name=${name}&page=${page}`
        )
        .then(({ data }) => {
          this.applicants = data;
        });
    }
  },
  mounted() {
    EventBus.$on("update-table", this.updateApplicantsList);
    EventBus.$on("filter-table", this.getApplicants);
    this.getApplicants(1, this.status, "", "");
  },
  destroyed() {
    EventBus.$off("update-table", this.updateApplicantsList);
    EventBus.$off("filter-table", this.getApplicants);
  }
};
</script>

